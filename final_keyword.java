final class Final_example{

    final int i = 10; //constant
    
    final void printable(){ 
        System.out.print("This is a final Method, means it cannot be overriden");
    }

}
/*FINAL KEYWORD
1.final Variable: The variable's value cannot be changed after initialization.
2.final Method: Method cannot be overriden by subclass
3.final class: The class cannot be subclassed.
 */