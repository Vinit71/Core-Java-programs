class DrivingLicense{
    public static void main(String[] args) {
        int age = 20;

        // Check if the person is eligible for driving
        if (age > 18 && age < 70) {
            System.out.println("You are eligible for driving.");
        } else {
            System.out.println("You are not eligible for driving.");
        }
    }
}

